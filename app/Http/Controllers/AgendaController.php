     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $agenda = new Agenda();
        $agenda->title = $request->event_name;
        $agenda->start = $request->start_date." ".$request->end_time;
        $agenda->end = $request->end_date." ".$request->end_time;
        $agenda->id_doctor = $request->idDoctor;
        $agenda->save();
        return $agenda;
    }

